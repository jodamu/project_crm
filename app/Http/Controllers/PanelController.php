    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
      
        $tablero=Panel::findOrFail($id)->tablero;
        
        if(count($tablero)>0){
            return redirect()->back()->with('status','El panel cuenta con tareas pendientes');
        }

        $panel = Panel::find($id);
       
        $panel->delete();


        return redirect()->back();
    }
